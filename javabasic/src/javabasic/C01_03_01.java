package javabasic;

//주석 사용하기
//주석은 프로그램 실행과는 상관없이 코드에 설명을 붙인 것을 말합니다.
//복잡한 코드일수록 주석을 달면
//전체 코드를 이해하기 쉽고 수정이 용이합니다.
//특히 다른 사람이 작성한 코드를 주석 없이 해석하는
//작업은 쉬운 일이 아니기 때문에
//본인이 작성한 코드를 다른 사람이 볼 필요가 있다면
//주석을 꼭 넣어주는 것이 좋습니다.
public class C01_03_01 {
	
	public static void main(String[] args) {
		//콘솔에 hello등의 문자를 출력한다
		System.out.println("Hello, Java 02");
	}

}
